# Install Homebrew package manager (https://brew.sh/)
/usr/bin/ruby -e "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/master/install)"

# Install tools from source with Homebrew
brew install git;       # Git
brew install node;      # Node.js
brew install python;    # Python 3

# Install pre-compiled applications with Homebrew Cask
brew cask install firefox;              # Mozilla Firefox
brew cask install google-chrome;        # Google Chrome
brew cask install slack;                # Slack
brew cask install visual-studio-code;   # Visual Studio Code
brew cask install spotify; 		# Spotify
brew cask install microsoft-edge;	# Edge
